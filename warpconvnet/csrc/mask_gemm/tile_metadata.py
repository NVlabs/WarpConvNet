# Runtime API — query helpers operating on the baked _TILES list.
# ---------------------------------------------------------------------------


def build_tile_metadata(
    *,
    active_only: bool = True,
    ops: Optional[Iterable[str]] = None,
) -> List[TileMetadata]:
    """Return the baked tile metadata, optionally filtered.

    Args:
        active_only: if True (default), return only production-tier tiles.
            Mirrors the live warpgemm contract; experimental + legacy
            tiers are baked too but excluded by default.
        ops: if given, restrict to this op subset (e.g. ``("forward",)``).

    Returns:
        New list — safe to mutate.
    """
    out = list(_TILES)
    if active_only:
        out = [m for m in out if m.tier == "production"]
    if ops is not None:
        wanted = set(ops)
        out = [m for m in out if m.op in wanted]
    return out


def get_schema_version() -> int:
    """Schema version of this generated module.

    Downstream consumers should pin a major-version compatibility check.
    """
    return SCHEMA_VERSION


def compiled_mw_template_boundaries(op: str) -> List[int]:
    """Distinct MaskWords template values compiled for this op.

    Returns sorted list. Empty for ``"wgrad"`` (computed at runtime, not
    a template parameter).
    """
    if op == "wgrad":
        return []
    if op not in {"forward", "dgrad"}:
        raise ValueError(f"unknown op {op!r}; expected 'forward', 'dgrad', or 'wgrad'")
    active = build_tile_metadata(active_only=True, ops=(op,))
    return sorted({m.mask_words for m in active})


def current_device_arch_code(device_index: int = 0) -> int:
    """Return SM arch code for the current CUDA device, e.g. 80, 89, 90."""
    import torch

    if not torch.cuda.is_available():
        raise RuntimeError(
            "current_device_arch_code() requires CUDA; " "torch.cuda.is_available()=False"
        )
    major, minor = torch.cuda.get_device_capability(device_index)
    return major * 10 + minor


def filter_by_device(
    metadata: Iterable[TileMetadata],
    device_index: int = 0,
) -> List[TileMetadata]:
    """Filter a tile metadata iterable to those compiled for the current device."""
    arch = current_device_arch_code(device_index)
    return [m for m in metadata if m.supports_arch(arch)]


class TileNotCompiledForDeviceError(RuntimeError):
    """Pinned tile_id isn't compiled for the current device."""


def check_tile_for_device(
    op: str,
    tile_id: int,
    device_index: int = 0,
) -> None:
    """Raise if ``tile_id`` isn't compiled for the current device's SM arch."""
    import torch

    if not torch.cuda.is_available():
        return
    arch = current_device_arch_code(device_index)
    for m in build_tile_metadata(active_only=False, ops=(op,)):
        if m.tile_id == tile_id and m.op == op:
            if not m.supports_arch(arch):
                raise TileNotCompiledForDeviceError(
                    f"tile_id={tile_id} ({m.kernel_struct}) is registered "
                    f"with compile_archs={m.compile_archs}, but the current "
                    f"device is SM_{arch}. Pick a tile that supports "
                    f"SM_{arch} or rebuild with TORCH_CUDA_ARCH_LIST set "
                    f"to an arch in {m.compile_archs}."
                )
            return


