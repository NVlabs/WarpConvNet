SCHEMA_VERSION = 6

# 128 bits / 16 bits per fp16 element. Translates scalar_a/b/epi
# flags into concrete channel-alignment requirements.
_KVEC = 8


@dataclass(frozen=True)
class TileMetadata:
    """Everything a dispatcher needs to decide whether a tile fits a problem.

    Invariants populated from the warpgemm registry at import time.
    See the module docstring for query examples.
    """

    # --- Identification --------------------------------------------------
    tile_id: int
    op: str  # "forward" | "dgrad" | "wgrad"
    kernel_struct: str
    tile_tag: str  # e.g. "Tile64x64x32" or "Tile64x128x32_F16Accum"
    comment: str
    tier: str  # "production" | "experimental" | "legacy"

    # --- MMA geometry ----------------------------------------------------
    tile_m: int
    tile_n: int
    tile_k: int
    num_stages: int
    mainloop: str
    epilogue: str  # fwd/dgrad always "direct"; wgrad has atomic/workspace/etc.

    # --- Numerics --------------------------------------------------------
    # `acc_dtype`: the DEFAULT accumulator if the caller doesn't set
    # `use_fp16_accum`. Kept for backward compat; prefer
    # `supported_accumulator_dtypes` which captures runtime switchability.
    acc_dtype: str  # "f16" | "f32"
    # `supported_accumulator_dtypes`: accumulator dtypes this tile can
    # dispatch at runtime via the `use_fp16_accum` binding flag. Most
    # base tiles (Tile*x*x*) have both f16 and f32 accum variants
    # compiled in; specialized tiles (`_F16Accum`, `_F16K8`, `_DgradK8`,
    # `_F32K8` suffixes) are compiled for one accum only.
    supported_accumulator_dtypes: Tuple[str, ...]  # subset of ("f16", "f32")
    supported_input_dtypes: Tuple[str, ...]  # subset of ("f16","bf16","f32")
    supported_output_dtypes: Tuple[str, ...]  # subset of ("f16","bf16","f32")

    # --- K / mask capability --------------------------------------------
    mask_words: int  # max K/32 the kernel iterates (template arg)

    # --- Alignment / shape constraints ----------------------------------
    # `c_{in,out}_alignment`: PERFORMANCE HINT. Fast-path alignment for
    # vectorized cp.async; below this the kernel falls back to scalar
    # loads (correct but slower). 1 = scalar path, 8 = vectorized.
    c_in_alignment: int
    c_out_alignment: int
    # `min_per_group_c_{in,out}`: CORRECTNESS GATE. The binding's
    # TORCH_CHECK threshold on padded-to-kVec value. A dispatcher must
    # ensure `((c_per_group + 7) // 8) * 8 >= min_per_group_c_*` or the
    # kernel launch raises RuntimeError. Value 1 means "any C >= 1 is
    # accepted" (no binding check); otherwise equals the tile's
    # vectorization threshold (typically tile_m or tile_n).
    min_per_group_c_in: int
    min_per_group_c_out: int

    # --- Capabilities ---------------------------------------------------
    supports_groups: bool  # single-launch groups via gridDim.z — true for all current tiles
    supports_identity_shortcut: bool  # submanifold K/2 dense path — fwd only
    supports_split_k: bool  # wgrad atomic-class epilogue — caller may request split_k > 1
    persistent: bool  # grid=num_SMs, dynamic tile claiming

    # --- Raw flags (for rare callers who need them) ---------------------
    scalar_a: bool
    scalar_b: bool
    scalar_epi: bool
    use_smem_epilogue: bool
    use_b_kblock: bool
    split_offsets: int
    min_blocks: int

    # --- Arch gating (v4.2, added 2026-04-20) ---------------------------
    # SM arch codes this tile was compiled for. A tile may be in the
    # registry but excluded from the current build if its compile_archs
    # didn't intersect TORCH_CUDA_ARCH_LIST at codegen time — in that case
    # it won't appear in the active metadata list at all. When present,
    # `supports_arch()` / `filter_by_device()` let runtime dispatchers
    # skip tiles that the .so has but the current device can't execute.
    compile_archs: Tuple[int, ...] = (80, 86, 89, 90)

    # --- MW dispatch authorization (v6, bond #35) -----------------------
    # `dispatch_mask_words`: the set of MaskWords values this tile is
    # VALIDATED-CORRECT to be instantiated/dispatched at. This is the
    # AUTHORIZATION axis, distinct from `mask_words` (the single canonical
    # compiled MW of this tile_id). A consumer that re-instantiates one
    # kernel_struct across multiple MW (warpconvnet's DISPATCH_MW) reads this
    # to know which MW are safe — it does NOT assert a binary exists in any
    # particular .so. flat/fused → (1,2,4,8,12); pcoff → (1,) (smem ceiling);
    # wgrad → (1,) (no MaskWords template param). See
    # `dispatch_mask_words_for` in codegen/registry.py for the rule.
    dispatch_mask_words: Tuple[int, ...] = (1,)

    # --- Query helpers --------------------------------------------------

    def supports_arch(self, arch_code: int) -> bool:
        """True if this tile was compiled for the given SM arch code.

        ``arch_code`` is the integer form, e.g. 80 for SM_80 (A100), 89
        for SM_89 (RTX 6000 Ada). See ``torch.cuda.get_device_capability()``
        → ``major * 10 + minor``.

        If this returns False on the device you're running on, ``_C.mask_gemm_*``
        will raise "unsupported tile_id" at dispatch. Prefer to filter via
        ``filter_by_device()`` at candidate-selection time.
        """
        return arch_code in self.compile_archs

    def supports_mask_words(self, mw: int) -> bool:
        """True if this tile was instantiated with ``MaskWords >= mw``.

        A kernel with ``MaskWords=1`` dispatched with a pair_mask sized for
        ``mw=4`` will silently drop offsets k>=32 — the MinkUNet regression
        from 2026-04-17.

        NOTE: this checks the canonical compiled MW of *this* tile_id. For
        consumers that re-instantiate a kernel_struct across MW values, use
        ``supports_dispatch_mask_words`` (the authorization axis) instead.
        """
        return self.mask_words >= mw

    def supports_dispatch_mask_words(self, mw: int) -> bool:
        """True if this tile is VALIDATED-correct to be dispatched at ``mw``.

        Authorization, not availability — see the ``dispatch_mask_words``
        field docstring. A consumer's MW1-only guard derives from this:
        ``{m.tile_id for m in build_tile_metadata() if not
        m.supports_dispatch_mask_words(2)}`` is the set of tiles that may
        only ever be dispatched at MaskWords=1.
        """
        return mw in self.dispatch_mask_words

    def handles_c_in(self, c_in: int, groups: int = 1) -> bool:
        """True if the binding accepts this (C_in, groups) combination.

        Correctness gate — the kernel may still take a scalar path
        internally for non-aligned values; what this returns tells you
        whether ``_C.mask_gemm_*`` will raise RuntimeError at dispatch.

        Op-specific (mirrors bindings.cpp):
        - forward / dgrad: padded-to-kVec per-group C must clear
          ``min_per_group_c_in`` (typically tile_m; permissive tiles
          accept any C >= 1).
        - wgrad (post-96be2fe): per-group C must be a multiple of kVec
          (strict), OR the tile has scalar_a=True (any C >= 1).

        For PERFORMANCE filtering, use ``prefers_vectorized_c_in``.
        """
        if groups <= 0 or c_in % groups != 0:
            return False
        per_group = c_in // groups
        if per_group < 1:
            return False
        if self.op == "wgrad":
            # Binding requires strict kVec alignment (or scalar_a → any)
            return per_group % self.min_per_group_c_in == 0
        padded = ((per_group + _KVEC - 1) // _KVEC) * _KVEC
        return padded >= self.min_per_group_c_in

    def handles_c_out(self, c_out: int, groups: int = 1) -> bool:
        """True if the binding accepts this (C_out, groups) combination.

        Same semantics as ``handles_c_in``. See that docstring.
        """
        if groups <= 0 or c_out % groups != 0:
            return False
        per_group = c_out // groups
        if per_group < 1:
            return False
        if self.op == "wgrad":
            return per_group % self.min_per_group_c_out == 0
        padded = ((per_group + _KVEC - 1) // _KVEC) * _KVEC
        return padded >= self.min_per_group_c_out

    def prefers_vectorized_c_in(self, c_in: int, groups: int = 1) -> bool:
        """True if C_in aligns to the fast cp.async path (performance hint)."""
        if groups <= 0 or c_in % groups != 0:
            return False
        return (c_in // groups) % self.c_in_alignment == 0

    def prefers_vectorized_c_out(self, c_out: int, groups: int = 1) -> bool:
        """True if C_out aligns to the fast cp.async path (performance hint)."""
        if groups <= 0 or c_out % groups != 0:
            return False
        return (c_out // groups) % self.c_out_alignment == 0

    def handles_dtypes(self, input_dtype: str, output_dtype: str) -> bool:
        """True if the (input, output) dtype pair is instantiated for this tile."""
        return (
            input_dtype in self.supported_input_dtypes
            and output_dtype in self.supported_output_dtypes
        )

    def handles_accumulator(self, acc_dtype: str) -> bool:
        """True if this tile advertises the requested accumulator.

        Coarse check — returns True iff ``acc_dtype`` is in
        ``supported_accumulator_dtypes``. For accurate dispatch
        filtering that accounts for the fp16-input-only restriction
        on ``_f16acc`` variants, use ``handles_dtype_acc_combo``
        (or just call ``accepts()``, which routes through it).
        """
        return acc_dtype in self.supported_accumulator_dtypes

    def handles_dtype_acc_combo(
        self,
        input_dtype: str = "f16",
        output_dtype: str = "f16",
        acc_dtype: str = "f32",
    ) -> bool:
        """Input-dtype-aware (input, output, acc) correctness check.

        ``emit_cpp.py`` emits the ``_f16acc`` alt variant ONLY for
        fp16 inputs (the ``"half_t" in d.input_type`` gate). The
        ``supported_accumulator_dtypes`` field is stated globally per
        tile — a base tile with dual accumulator lists both ``f16``
        and ``f32``, but the f16 variant physically exists only when
        the input dtype is fp16.

        This method mirrors the emit rule: returns True iff the
        specific ``(input, output, acc)`` triple has a matching
        compiled instantiation.

        Dispatchers should prefer this (or ``accepts()``) over
        calling ``handles_dtypes`` + ``handles_accumulator``
        independently. Stage 3's validator
        (``validate_metadata_matches_instantiations``) pins this rule.
        """
        if not self.handles_dtypes(input_dtype, output_dtype):
            return False
        if acc_dtype not in self.supported_accumulator_dtypes:
            return False
        # What acc does the suffixless variant represent?
        # Dual-compiled tile (2 entries): suffixless = f32 (default),
        #   `_f16acc` alt = f16 variant (fp16-input only).
        # Single entry: that IS the suffixless variant.
        if len(self.supported_accumulator_dtypes) == 2:
            suffixless_acc = "f32"
        else:
            suffixless_acc = self.supported_accumulator_dtypes[0]
        # Suffixless variant works for every supported input dtype.
        if acc_dtype == suffixless_acc:
            return True
        # The `_f16acc` alt variant is fp16-input-only.
        return input_dtype == "f16"

    # -------------------------------------------------------------------
    # v4 consolidated gate + dispatch queries (added 2026-04-18).
    # -------------------------------------------------------------------

    def accepts(
        self,
        C_in: int,
        C_out: int,
        K: int,
        groups: int = 1,
        input_dtype: str = "f16",
        output_dtype: str = "f16",
        acc_dtype: str = "f32",
    ) -> bool:
        """Single consolidated correctness gate.

        Returns True iff ``_C.mask_gemm_{fwd,dgrad,wgrad}`` will accept
        the dispatch AND produce mathematically correct output. False
        means either the binding's TORCH_CHECK will reject, the tile
        wasn't instantiated for this dtype/MW combo, or the kernel
        wouldn't produce correct output.

        Composes ``supports_mask_words``, ``handles_c_in``,
        ``handles_c_out``, ``handles_dtypes``, ``handles_accumulator``
        — returning True here means all five agree.

        Dispatchers should prefer this over the individual helpers.
        """
        mw_needed = (K + 31) // 32
        return (
            self.supports_mask_words(mw_needed)
            and self.handles_c_in(C_in, groups)
            and self.handles_c_out(C_out, groups)
            and self.handles_dtype_acc_combo(input_dtype, output_dtype, acc_dtype)
        )

    def required_mask_words_for_K(self, K: int) -> int:
        """pair_mask stride the caller must allocate.

        The kernel reads ``pair_mask[voxel * MW + word]`` where MW is
        the compiled template constant (``self.mask_words``).  Caller
        must allocate ``pair_mask = zeros(N_out * MW, dtype=int32)``
        regardless of the runtime K — the kernel iterates up to
        ``self.mask_words * 32`` offsets.

        Raises ``ValueError`` if ``K`` exceeds this tile's compiled
        MW capacity — i.e. dispatch would silently drop offsets.
        """
        mw_needed = (K + 31) // 32
        if mw_needed > self.mask_words:
            raise ValueError(
                f"tile {self.tile_id} ({self.kernel_struct}) compiled "
                f"with mask_words={self.mask_words}; K={K} requires "
                f"mask_words>={mw_needed}. Use accepts() to pre-filter "
                f"candidates."
            )
        return self.mask_words

    def valid_split_k(self, N_out: int, K: int) -> List[int]:
        """Canonical split_k values this tile supports at this shape.

        Returns a sorted list starting at 1. Empty list for tiles that
        don't support split_k (all fwd/dgrad tiles; the wgrad
        ``direct`` epilogue).

        Values are capped by ``ceil(N_out / tile_m)`` — splitting past
        the number of row-tiles is a no-op. The per-epilogue canonical
        set reflects the useful range: ``atomic`` scales to 128;
        ``strided_atomic`` caps at 64 (locality); ``workspace``
        caps at 32 (memory-bound).
        """
        if self.op != "wgrad":
            return []
        base: List[int] = {
            "direct": [1],
            "atomic": [1, 2, 4, 8, 16, 32, 64, 128],
            "strided_atomic": [1, 4, 16, 64],
            "semaphore": [1, 4, 16, 32, 64],
            "workspace": [1, 4, 16, 32],
        }.get(self.epilogue, [1])
        max_split = max(1, (N_out + self.tile_m - 1) // self.tile_m)
        return [sk for sk in base if sk <= max_split]


